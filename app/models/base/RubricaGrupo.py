# Imports padr√µes
from datetime import datetime
from typing import TYPE_CHECKING, List

# Imports de terceiros
from sqlalchemy.orm import Mapped, mapped_column

# Imports locais
from app.config.db import db

# =================================================================================================================================================================
# Type para o mapping de colunas
# =================================================================================================================================================================

if TYPE_CHECKING:
    from app.models.base.Rubrica import Rubrica

# =================================================================================================================================================================
# Model de RubricaGrupo
# =================================================================================================================================================================

class RubricaGrupo(db.Model):
    # Define o nome da tabela no banco de dados
    __tablename__ = 'SGDP_RUBRICAS_GRUPOS'

    # Define as colunas da tabela
    id:                 Mapped[int] = mapped_column(primary_key=True)
    descricao:          Mapped[str]
    atualizado_em:      Mapped[datetime]
    criado_em:          Mapped[datetime]

# =================================================================================================================================================================
# Relacionamentos com as outras tabelas
# =================================================================================================================================================================

    rubricas:          Mapped[List["Rubrica"]] = db.relationship("Rubrica", back_populates='grupo_rubrica')