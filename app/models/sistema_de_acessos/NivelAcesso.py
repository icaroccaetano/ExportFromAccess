# Imports padr√µes
from datetime import datetime
from typing import TYPE_CHECKING, List

# Imports de terceiros
from sqlalchemy.orm import Mapped, mapped_column

# Imports locais
from app.config.db import db

# =================================================================================================================================================================
# Type para o mapping de colunas
# =================================================================================================================================================================

if TYPE_CHECKING:
    from app.models.Acesso import Acesso

# =================================================================================================================================================================
# Model de NivelAcesso
# =================================================================================================================================================================

class NivelAcesso(db.Model):
    # Define o nome da tabela no banco de dados
    __tablename__ = 'SGDP_NIVEIS_ACESSOS'

    # Define as colunas da tabela
    id:                 Mapped[int] = mapped_column(primary_key=True)
    nivel:              Mapped[int]
    atualizado_em:      Mapped[datetime]
    criado_em:          Mapped[datetime]

# =================================================================================================================================================================
# Relacionamentos com as outras tabelas
# =================================================================================================================================================================

    acessos:            Mapped[List["Acesso"]] = db.relationship('Acesso', back_populates='nivel_acesso')